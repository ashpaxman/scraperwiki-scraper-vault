def parse_list_page(doc)
  result = []
  anchors = doc.xpath('//*[@id="main"]//tr[1]/td[2]/a')

  # Note tr[1] is actually a table header
  anchors.each do |a|
    next if a["href"].nil? 
    next if a["href"].include?("#")
    next unless a["href"].include?("detail")
    result << {
      'url' => a["href"]
    }
  end

  result
end

